#Python script to conver an integer to HEX and OCTAL equivalent
n=int(input('Enter a number: '))
print ('Hexadecimal equivalent of' + str(n) + ": " + str(hex(n)))
print ('Octal equivalent of' + str(n) + ": " + str(oct(n)))
print ('Octal equivalent of' + str(n) + ": " + str(bin(n)))
print ('Sqaure root of ' + str(n) + ": " + str(n**0.5))
